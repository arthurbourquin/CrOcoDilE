# Array
fruits = ["apple", "banana", "cherry"]
print(fruits)

# Array length
fruits = ["apple", "banana", "cherry"]
print(len(fruits))

# Loop ("line break at each component")
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)

# double loop
fruits = ["apple", "banana", "cherry"]
for x in fruits:
	for y in x:
  		print(y)
